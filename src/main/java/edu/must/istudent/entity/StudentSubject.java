package edu.must.istudent.entity;

/**
 *
 * @author HiTech
 */
public class StudentSubject {

    private String code;
    private int credit;
    private int score;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public int getCredit() {
        return credit;
    }

    public void setCredit(int credit) {
        this.credit = credit;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    @Override
    public String toString() {
        return "StudentSubject{" + "code=" + code + ", credit=" + credit + ", score=" + score + '}';
    }

}
